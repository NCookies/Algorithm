WITH TBL AS (
    SELECT CAR_ID
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE START_DATE BETWEEN "2022-08-01" AND "2022-10-31"
    GROUP BY CAR_ID
    HAVING COUNT(*) >= 5
)

SELECT
    MONTH(START_DATE),
    CAR_ID,
    COUNT(*) AS RECORDS
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE CAR_ID IN (SELECT CAR_ID FROM TBL) AND (MONTH(START_DATE) BETWEEN 8 AND 10)
GROUP BY 1, 2
HAVING RECORDS != 0
ORDER BY 1, 2 DESC
